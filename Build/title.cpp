static float	effect_dy;


//=============================================================================
// 初期化処理
//=============================================================================
HRESULT InitTitle(void)
{
	ID3D11Device *pDevice = GetDevice();

	//テクスチャ生成
	for (int i = 0; i < TEXTURE_MAX; i++)
	{
		g_Texture[i] = NULL;
		D3DX11CreateShaderResourceViewFromFile(GetDevice(),
			g_TexturName[i],
			NULL,
			NULL,
			&g_Texture[i],
			NULL);
	}


	// 頂点バッファ生成
	D3D11_BUFFER_DESC bd;
	ZeroMemory(&bd, sizeof(bd));
	bd.Usage = D3D11_USAGE_DYNAMIC;
	bd.ByteWidth = sizeof(VERTEX_3D) * 4;
	bd.BindFlags = D3D11_BIND_VERTEX_BUFFER;
	bd.CPUAccessFlags = D3D11_CPU_ACCESS_WRITE;
	GetDevice()->CreateBuffer(&bd, NULL, &g_VertexBuffer);



	// 変数の初期化
	g_Use   = TRUE;
	g_w     = TEXTURE_WIDTH;
	g_h     = TEXTURE_HEIGHT;
	g_Pos   = XMFLOAT3(g_w/2, g_h/2, 0.0f);
	g_TexNo = 0;

	alpha = 1.0f;
	flag_alpha = TRUE;

	effect_dx = 100.0f;
	effect_dy = 100.0f;

	choice = 0;

	g_Load = TRUE;
	return S_OK;
}

//=============================================================================
// 終了処理
//=============================================================================
void UninitTitle(void)
{
	if (g_Load == FALSE) return;

	if (g_VertexBuffer)
	{
		g_VertexBuffer->Release();
		g_VertexBuffer = NULL;
	}

	for (int i = 0; i < TEXTURE_MAX; i++)
	{
		if (g_Texture[i])
		{
			g_Texture[i]->Release();
			g_Texture[i] = NULL;
		}
	}

	g_Load = FALSE;
}

//=============================================================================
// 更新処理
//=============================================================================
void UpdateTitle(void)
{

	if (GetKeyboardTrigger(DIK_RETURN)|| IsButtonTriggered(0, BUTTON_M)|| IsButtonTriggered(0, BUTTON_C))
	{// Enter押したら、ステージを切り替える
		switch (choice)
		{
		case 0:
			SetFade(FADE_OUT, MODE_TUTORIAL);
			break;

		case 1:
			SetInfMode(FALSE);
			SetFade(FADE_OUT, MODE_GAME);

			break;

		case 2:
			SetInfMode(TRUE);
			SetFade(FADE_OUT, MODE_GAME);

			break;

		case 3:

			SetEnd();

			break;

		default:
			break;
		}


	}


	if (IsButtonTriggered(0, BUTTON_UP)|| GetKeyboardTrigger(DIK_UP))
	{
		choice -= 1;
		if (choice<0)
		{
			choice = 3;
		}
	}

	if (IsButtonTriggered(0, BUTTON_DOWN) || GetKeyboardTrigger(DIK_DOWN))
	{
		choice += 1;
		choice %= 4;
	}




#ifdef _DEBUG	// デバッグ情報を表示する
	//PrintDebugProc("Player:↑ → ↓ ←　Space\n");

#endif

}

